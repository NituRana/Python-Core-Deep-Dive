# Integer Operations
print("Integer Operations:")
x = 10
y = 3
print(f"x: {x}, y: {y}")
print(f"Addition: {x + y}")
print(f"Subtraction: {x - y}")
print(f"Multiplication: {x * y}")
print(f"Division: {x / y}")  # Results in float
print(f"Floor Division: {x // y}")
print(f"Modulus: {x % y}")
print(f"Power: {x ** y}")

# Float Operations
print("\nFloat Operations:")
a = 5.7
b = 2.3
print(f"a: {a}, b: {b}")
print(f"Addition: {a + b}")
print(f"Type Casting to Integer: int(a) = {int(a)}")

# String Operations
print("\nString Operations:")
s1 = "Hello"
s2 = "World"
print(f"s1: {s1}, s2: {s2}")
print(f"Concatenation: {s1 + ' ' + s2}")
print(f"Repetition: {s1 * 3}")
print(f"Character at index 1: {s1[1]}")
print(f"Slicing: {s1[1:4]}")
print(f"Uppercase: {s1.upper()}")
print(f"Split: {s1.split('e')}")

# Boolean Logic
print("\nBoolean Logic:")
is_raining = True
is_sunny = False
print(f"is_raining: {is_raining}, is_sunny: {is_sunny}")
print(f"is_raining and is_sunny: {is_raining and is_sunny}")
print(f"is_raining or is_sunny: {is_raining or is_sunny}")
print(f"not is_raining: {not is_raining}")